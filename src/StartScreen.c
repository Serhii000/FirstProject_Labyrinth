void startscreen()
{   

    char ** mas = (char**)malloc(sizeof(char*) * 14);
    
    mas[0] = "##########################################################################################################";
    mas[1] = "##########################################################################################################";
    mas[2] = "########~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~########";
    mas[3] = "######### ====================================================================================== #########";
    mas[4] = "######### =   LL         AAA    BBBBB    Y    Y   RRRRR    II   NNN   NN   TTTTTTTT   HH   HH  = #########";
    mas[5] = "######### =   LL       AA   A   BB   B    Y  Y    RR   R   II   NN N  NN      TT      HH   HH  = #########";
    mas[6] = "######### =   LL       AAaaaA   BBBBB      YY     RRrrR    II   NN  N NN      TT      HHHHHHH  = #########";
    mas[7] = "######### =   LL       AA   A   BB   B     YY     RR  R    II   NN  N NN      TT      HH   HH  = #########";
    mas[8] = "######### =   LLLLLL   AA   A   BBBBB      YY     RR   R   II   NN   NNN      TT      HH   HH  = #########";
    mas[9] = "######### ====================================================================================== #########";
    mas[10] = "########~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~########";
    mas[11] = "##########################################################################################################";
    mas[12] = "################################          PRESS ENTER          ###########################################";
    mas[13] = "##########################################################################################################";
    

    int c = 3, mas_c = 0;

    while( c != 17 )
    {
        mvprintw(c, 0, mas[mas_c]);
        c++;
        mas_c++;
    }

    refresh();

    int key = 0;

    while ( key != K_ENTER )
    {
        key = getchar();
    }

    clear();

}
