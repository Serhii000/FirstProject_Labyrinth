#include "../inc/LIB_FUN.h"

int menu()
{
    int y_pos = 9;
    int key;

    while (true)
    {
        mvprintw(0, 3, "########################################################################");
        mvprintw(1, 3, "#                                 ###                                  #");
        mvprintw(2, 3, "#   ########################      ###       ########################   #");
        mvprintw(3, 3, "#   #                             ###                              #   #");
        mvprintw(4, 3, "#   #   ########################################################   #   #");
        mvprintw(5, 3, "#   #   #                                                      #   #   #");
        mvprintw(6, 3, "#   #   #   ================================================   #   #   #");
        mvprintw(7, 3, "#   #   #   | ############################################||   #   #   #");
        mvprintw(8, 3, "#   #   #   ||#                                          #||   #   #   #");
        mvprintw(9, 3, "#   #   #   ||#               go to the game             #||   #   #   #");
        mvprintw(10, 3, "#   #####   ||#                description               #||   #####   #");
        mvprintw(11, 3, "#   #####   ||#                developers                #||   #####   #");
        mvprintw(12, 3, "#   #   #   ||#                   exit                   #||   #   #   #");
        mvprintw(13, 3, "#   #   #   ||#                                          #||   #   #   #");
        mvprintw(14, 3, "#   #   #   |##############################################|   #   #   #");
        mvprintw(15, 3, "#   #   #   ================================================   #   #   #");
        mvprintw(16, 3, "#   #   #                                                      #   #   #");
        mvprintw(17, 3, "#   #   ########################################################   #   #");
        mvprintw(18, 3, "#   #                             ###                              #   #");
        mvprintw(19, 3, "#   ########################      ###       ########################   #");
        mvprintw(20, 3, "#                                 ###                                  #");
        mvprintw(21, 3, "########################################################################");

        mvprintw(y_pos, 25, ">>>");
        refresh();

        key = getchar();

        switch (key)
        {
        case K_DOWN:
            if (y_pos < 12)
                y_pos = y_pos + 1;
            break;

        case K_UP:
            if (y_pos > 9)
                y_pos = y_pos - 1;
             break;

        case K_ENTER:
            clear();

            if (y_pos == 12)
                return 0;

            if (y_pos == 11)
                print_dev();

            if (y_pos == 10)
                print_des();

            if (y_pos == 9)
                return 9;
        }

        for ( int i = 9; i < 12; i++ )
        {
            mvprintw(i, 25, "   ");
        }
    }
}